#pragma once

#include <SFML\Graphics.hpp>

typedef sf::Vector2f vec2;

//////////////////////////////////////////////////////////////////////////

float Length(vec2 v)
{
	return sqrt((v.x * v.x) + (v.y * v.y));
}

//////////////////////////////////////////////////////////////////////////

float LengthSq(vec2 v)
{
	return ((v.x * v.x) + (v.y * v.y));
}

//////////////////////////////////////////////////////////////////////////

vec2 Normalize(vec2 v)
{
	float length = Length(v);
	return vec2(v.x / length, v.y / length);
}

